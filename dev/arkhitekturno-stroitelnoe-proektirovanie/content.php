<ul style="list-style-type: circle;">
<li>Строительное проектирование — анализ архивных данных, комплекс исследовательских работ, подготовка необходимой документации;</li>
<li>Разработка индивидуальных макетов инженерных систем различного типа и назначения;</li>
<li>Инженерно-техническое обследование зданий;</li>
<li>Реконструкторские услуги;</li>
<li>Услуги авторского надзора;</li>
<li>Планировка и застройка микрорайонов: анализ актуальной обстановки, выполнение необходимых модернизационных мероприятий.</li>
