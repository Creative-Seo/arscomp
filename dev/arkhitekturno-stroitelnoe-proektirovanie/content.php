        <ul style="list-style-type: circle;">
    <li>Создание начального плана;</li>
    <li>Глубокое исследование различных характеристик земельного участка, где впоследствии будет размещен объект;</li>
    <li>Разработка схемы размещения с использованием 3D-технологий;</li>
