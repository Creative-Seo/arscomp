<p>Лаборатория экологического мониторинга представляет собой автоматическую передвижную станцию контроля за состоянием окружающей среды. Исследовательское оборудование размещается на базе цельнометаллического фургона и включает в себя комплект высокотехнологичных приборов и оборудования, предназначенных для сбора и анализа образцов почвы, воды и воздуха. Услуги передвижной экологической лаборатории — необходимая мера контроля над уровнем соответствия промышленной деятельности стандартам и требованиям охраны окружающей среды.&nbsp;</p>
<h2>ООО ОКП «АРС» предлагает Вам услуги экологической лаборатории.&nbsp;</h2>
        <ul style="list-style-type: circle;">
    <li>Измерение концентрации загрязняющих веществ в воздухе рабочей зоны;</li>
    <li>Исследования метеопараметров в фоновом режиме;</li>
    <li>Экспресс-анализ физических и химических свойств воды и почвы на территории промышленных предприятий;</li>
    <li>Исследования промышленных выбросов и уровня радиации.&nbsp;</li>
        </ul>
<h2>Область применения данных, полученных в лаборатории экологического контроля:</h2>
        <ul style="list-style-type: circle;">
    <li>Наблюдения за окружающей средой;</li>
    <li>Экологический мониторинг производственных объектов;</li>
    <li>Систематический экомониторинг на территории населенных пунктов;</li>
    <li>Контроль состава промышленных выбросов;</li>
    <li>Контроль радиационной обстановки;</li>
    <li>Соблюдение санитарно-гигиенических норм на производстве;</li>
    <li>Полная проверка объекта на предмет соответствия стандартам экологической безопасности.</li>
        </ul>
<h2>Наши преимущества:</h2>
