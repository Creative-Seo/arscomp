<p>Контроль над качеством электросетей — задача, не теряющая своей актуальности. Современная электротехническая лаборатория позволяет получить наиболее точные результаты электротехнических измерений.</p>
<p>ООО ОКП «АРС» предлагает Вам услуги передвижной электротехнической лаборатории.</p>
<p>Перечень основных услуг электротехнической лаборатории:&nbsp;</p>
<ul style="list-style-type: circle;">
<li>Измерения сопротивления изоляции аппаратов и проводящих материалов в электроустановках;</li>
<li>Топографическое определение трассы кабельных линий;</li>
<li>Выявление и устранение дефектов кабельных линий;</li>
<li>Проверка наличия цепи между заземлителем и заземленными элементами;</li>
<li>Испытания силовых и контрольных кабелей на напряжение до 1000В и выше;</li>
<li>Испытания электротехнических конструкций повышенным напряжением;</li>
<li>Измерения переходных сопротивлений постоянному току контактов шин, распределительных устройств;</li>
<li>Испытание защитных средств;</li>
<li>Проверка силовых трансформаторов и трансформаторного масла;</li>
<li>Качественно-количественный анализ электросетей объекта</li>
</ul>
<p>Результат работы электротехнической лаборатории — предоставление заказчику подробного технического отчета о проведенных испытаниях.&nbsp;</p>
<p>Наши преимущества:</p>
<ul style="list-style-type: circle;">
<li>Команда квалифицированных специалистов-электротехников;</li>
<li>Мобильная лаборатория на базе цельнометаллического фургона;</li>
<li>Современное цифровое оборудование (рефлектометры, блоки высоковольтных испытаний и прожига, электрогенераторы и т.д.);</li>
<li>Качественное выполнение работ, разумные сроки, доступные цены.</li>
</ul>
<p>Услуги электротехнической лаборатории доступны после заключения договора с заказчиком — физическим или юридическим лицом. Каждая электротехническая лаборатория обладает лицензией и пакетом необходимой разрешительной документации.&nbsp;</p>
