<p>Работа в лаборатории — важнейший этап инженерно-геологического изыскания. Лабораторный анализ данных, полученных в результате геологического изыскания, позволит получить ясное представление о реализации будущего строительного проекта. Глубокое исследование свойств грунтов, подземных и сточных вод позволит с максимальной точностью предугадать характер их взаимодействия с материалами фундамента сооружения. Благополучные результаты инженерно-геологического исследования — необходимое условие для успешного строительства.</p>
<h2>В перечень услуг инженерно-геологической лаборатории, предоставляемых ООО ОКП «АРС», входят:</h2>
<ul style="list-style-type: circle;">
<li>Комплекс исследований, направленных на изучение физических и механических свойств грунтов;</li>
<li>Определение количественных показателей природных грунтов;</li>
<li>Анализ сточных и подземных вод;</li>
<li>Глубокий химических анализ грунтов и воды;</li>
<li>Мониторинг коррозионной активности грунтов и воды по отношению к бетону и металлам;&nbsp;</li>
<li>Испытание стройматериалов;</li>
<li>Оптимальный подбор строительных смесей;</li>
<li>Мониторинг и прогнозирование вероятных геологических изменений, сопутствующих возведению и последующей эксплуатации объекта.&nbsp;</li>
</ul>
<p>Геологическая лаборатория ООО ОКП «АРС» оснащена современным цифровым оборудованием, позволяющим добиться максимально точных результатов исследования, а также уложиться в разумные сроки. Все лабораторное оборудование регулярно проходит плановую проверку и подвергается техническому надзору. Наши специалисты имеют высокую квалификацию и солидный опыт в области инженерно-геологических исследований.&nbsp;</p>
