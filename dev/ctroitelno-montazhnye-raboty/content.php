<p>Особое Конструкторское Подразделение «АРС» предлагает услуги по проведению строительно-монтажных работ.&nbsp;</p>
<p><img style="float: right;" src="/images/matc/stroyka/stroitelstvo-okpars.ru-02.jpg" alt="" width="300" class="size-auto"></p>
            <ul style="list-style-type: circle;">
        <li>Строительство зданий и сооружений;</li>
        <li>Подготовка оснований, фундаментов и опорных конструкций под оборудование;</li>
        <li>Подготовка земельного участка к последующему строительству;</li>
        <li>Озеленение и благоустройство;</li>
        <li>Санитарно-технические и электромонтажные услуги.&nbsp;</li>
            </ul>
        <h4>Подробный перечень услуг:&nbsp;</h4>
            <ul style="list-style-type: circle;">
        <li>Земляные работы. Подготовка грунта к возведению опорных конструкций и фундаментов, рытье траншей для подземных коммуникаций и т.д..</li>
        <li>Свайные работы. Устройство свайных фундаментов, погружение, забивка свай.</li>
        <li>Каменные работы. Возведение каменных конструкций (строительство домов) из соответствующих материалов — штучные&nbsp;блоки, кладка, кирпич, искусственный камень и т.д.</li>
        <li>Бетонные и железобетонные работы. Возведение конструкций, подготовка, транспортировка и укладка смеси, создание необходимых условий для затвердения;</li>
        <li>Комплекс монтажных работ. Доставка, установка, подгонка и закрепление составных элементов и готовых деталей конструкций.&nbsp;</li>
        <li>Кровельные работы.<img style="float: right;" src="/images/matc/stroyka/stroitelstvo-okpars.ru-04.jpg" alt="" width="300" class="size-auto"></li>
        <li>Отдельные работы. Облицовка, окраска, оштукатуривание, оклейка&nbsp;<span style="line-height: 1.3em;">обоями интерьеров и экстерьеров.&nbsp;</span></li>
        <li>Комплекс плотничных и столярных работ. Возведение конструкций из готовых деталей, элементов или соответствующих материалов (доски, брус и т.п.), также настилка дощатых полов и паркета.&nbsp;</li>
        <li>Специальные работы. Все, что касается специфических материалов и способов производства. Монтаж защитных покрытий, устройство сетей связи, санитарно-технические системы, элеваторы.&nbsp;</li>
        <li>Комплекс транспортных и погрузочно-разгрузочных услуг. Доставка материалов, деталей и конструкций, необходимого строительного инвентаря и инструмента. Работа с грузоперевозочным, подвесным, конвейерным транспортом.&nbsp;</li>
            </ul> 		
