<h4>Основными преимуществами технологии смк, доказанными практикой, по отношению к другим технологиям домостроения являются:</h4>
<p><img style="float: right;" src="/images/matc/karkaz/dom.jpg" alt="" width="300" height="300" class="size-auto"></p>
<ul style="list-style-type: circle;">
<li>Стоимость материалов. Расход основного сырья и материалов (цемент, щебень, арматура) в 1,5 раза меньше, чем при монолитном и панельном домостроении;</li>
<li>Увеличение полезной площади на 15-20% в сравнении с кирпичным исполнением;</li>
<li>Снижение стоимости строительства несущих конструкций здания с учетом возврата затрат от увеличения площади;</li>
<li>Полное отсутствие сварочных работ на стройплощадке и невысокие требования к квалификации рабочего персонала из-за меньшей трудоемкости как на технологической линии по выпуску элементов СМК, так и на стройплощадке;</li>
<li>Сокращение в 1,5-2 раза сроков строительства по сравнению с монолитным и кирпичным строительством (например, одним краном в месяц монтируется до 7тыс. кв. м полносборного каркаса с готовностью 95 %);</li>
<li>Исключение фактора хищения бетона, арматуры на строительных площадках;</li>
<li>Изготовление элементов СМК в заводских условиях, что гарантирует высокое их качество и исключает человеческий фактор;</li>
<li>Возможность размещения подземной авто стоянки под зданием;</li>
<li>Уменьшение веса несущих конструкций;</li>
<li>возможность использования различных местных материалов при изготовлении наружных стен;</li>
<li>Неограниченные возможности перепланировки помещений в период проектирования, строительства и эксплуатации;</li>
<li>Быстрая переналадка технологического оборудования под запросы рынка;</li>
<li>Небольшой вес конструкций и, как следствие, отсутствие на стройплощадке тяжелых башенных кранов с большой грузо-подъемностью;</li>
<li>Сокращение транспортных расходов;</li>
<li>Универсальность элементов, что позволяет их использование при любых архитектурных и конструктивных решениях при программном проектировании.</li>
</ul>
