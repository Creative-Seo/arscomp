<span style="line-height: 1.3em;">На оголовке стрелы расположены четыре колеса, предназначенные для переключения крана между транспортным и рабочим режимом без помощи вспомогательного устройства. Все элементы стрелы и гуськов имеют быстроразъемное пальцевое соединение.</span></p>
<p>При работах с сыпучими грузами на стрелу подвешивается двухканатный грейфер вместимостью до двух кубометров.&nbsp;</p>
<p>На стрелу может быть монтирован управляемый или неуправляемый гусек-удлинитель. При работе с башенно-стреловым оборудованием на башню устанавливается управляемый гусек длиной от 10 до 20 метров (конечная длина определяется базовой длиной башни).</p>
