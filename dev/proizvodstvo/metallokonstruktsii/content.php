<h2>Производство металлокнструкций любой сложности</h2>
<p>Цех металлоконструкций нашей компании оснащен новейшим западным оборудованием и может изготовить металлоконструкции по индивидуальным чертежам&nbsp;любой точности и сложности.</p>
<p>Мы работаем в тесном взаимодействии с заказчиком, принимаем решения в кратчайшие сроки.</p>
<p>В настоящее время мощьность нащего производства составляет 1000 тонн металлоконструкций в месяц, в течении года планируем увеличить мощьность до 2000 тонн.</p>
<p>В процессе производства все изделя, производимые на нашем предприятии, проходят обязательную дробеструйную обработку на специальном оборудовании.&nbsp;Возможность обработки листового металлопроката на современном оборудовании с ЧПУ с газокислородной и плазменной резкой позволяет выполнять по программе изготовление элементов сложной конфигурации с высокой производительностью и качеством, удовлетворяющим требования заказчика.&nbsp;Все сварные соединения металлоконструкций осуществляются при помощи автоматической сварки в защитных газах (по спецификации и в строгом соответствии с технической документацией заказчика).</p>
