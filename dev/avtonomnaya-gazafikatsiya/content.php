<li>Удобные сроки установки и легкость эксплуатации;</li>
<li>Возможность самостоятельного регулирования расходов топлива и горячей воды;</li>
<li>Полная независимость от коммунальных сетей;</li>
<li>Стабильность цен на топливо;</li>
<li>Последовательная экономия средств;&nbsp;</li>
<li>Экологичность.</li>
</ul>
